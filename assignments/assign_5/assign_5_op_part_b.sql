CREATE TABLE
    Worker (
        WORKER_ID NUMBER PRIMARY KEY,
        FIRST_NAME VARCHAR2 (50),
        LAST_NAME VARCHAR2 (50),
        SALARY NUMBER,
        JOINING_DATE DATE,
        DEPARTMENT VARCHAR2 (50)
    );

