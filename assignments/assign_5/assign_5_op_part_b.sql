-- q1
SELECT
    UPPER(FIRST_NAME) AS WORKER_FIRSTNAME
FROM
    Worker;

-- q2
SELECT
    SUBSTR (FIRST_NAME, 1, 3) AS FIRST_THREE_CHARACTERS
FROM
    Worker;

-- q3
SELECT
    INSTR (FIRST_NAME, 'A') AS POSITION_OF_A
FROM
    Worker
WHERE
    FIRST_NAME = 'Amitabh';

-- q4
SELECT
    FIRST_NAME || ' ' || LAST_NAME AS COMPLETE_NAME
FROM
    Worker;

-- q5
SELECT
    *
FROM
    Worker
ORDER BY
    FIRST_NAME ASC,
    DEPARTMENT DESC;

-- q7
SELECT
    *
FROM
    Worker
WHERE
    FIRST_NAME LIKE '%A';

-- q9
SELECT
    COUNT(*) AS Employee_Count
FROM
    Worker
WHERE
    DEPARTMENT = 'Admin';

-- q10
SELECT
    DEPARTMENT,
    COUNT(*) AS num_of_workers
FROM
    Worker
GROUP BY
    DEPARTMENT
ORDER BY
    num_of_workers DESC;

